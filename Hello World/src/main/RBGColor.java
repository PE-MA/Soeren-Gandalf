package main;

public class RBGColor {
	String name = "";
	int red = 0;
	int blue = 0;
	int green = 0;
	
	public RBGColor(String ColorName, int redValue, int blueValue, int greenValue)
	{
		this.name = ColorName;
		this.red = redValue;
		this.blue = blueValue;
		this.green = greenValue;
	}
}